function getTotalSupply() {
  try {
    contract.totalsupply().then((res) =>
      State.update({
        totalSupply: ethers.utils.formatUnits(res, "wei"),
      })
    );
  } catch (error) {
    console.error("Error calling the function:", error);
  }
}

