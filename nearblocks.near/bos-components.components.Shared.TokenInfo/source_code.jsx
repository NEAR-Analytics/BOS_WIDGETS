function MainComponent(props) {
  const { network, contract, amount, decimals } = props;
  const [meta, setMeta] = useState({} );
  const config = getConfig(network);
  const Loader = (props) => {
    return (
      <div
        className={`bg-gray-200 h-5 rounded shadow-sm animate-pulse ${props.className}`}
      ></div>
    );
  };

  useEffect(() => {
    function fetchMetadata(contract) {
      if (contract) {
        asyncFetch(`${config?.rpcUrl}`, {
          method: 'POST',
          body: JSON.stringify({
            jsonrpc: '2.0',
            id: 'dontcare',
            method: 'query',
            params: {
              request_type: 'call_function',
              finality: 'final',
              account_id: contract,
              method_name: 'ft_metadata',
              args_base64: '',
            },
          }),
          headers: {
            'Content-Type': 'application/json',
          },
        })
          .then(
            (data



) => {
              const resp = data?.body?.result;
              setMeta(decodeArgs(resp.result));
            },
          )
          .catch(() => {});
      }
    }

    fetchMetadata(contract);
  }, [contract, config?.rpcUrl]);

  return !meta ? (
    <Loader wrapperClassName="flex w-full max-w-xl" />
  ) : (
    <>
      <span className="font-normal px-1">
        {tokenAmount(amount, meta.decimals || decimals, true)}
      </span>
      <span className="flex items-center">
        <TokenImage src={meta.icon} alt={meta.name} className="w-4 h-4 mx-1" />
        {shortenToken(meta.name)}
        <span>&nbsp;({shortenTokenSymbol(meta.symbol)})</span>
      </span>
    </>
  );
}

return MainComponent(props, context);